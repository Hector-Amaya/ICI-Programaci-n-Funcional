void main(List<String> args) {
  //incrementos y decrementos

  var contador = 10;

  contador = contador-1;
  print(contador);
  contador -=1;
  print(contador);
  contador--;
  print(contador);
  --contador;
  print(contador);


  var c = 10;
  print(++c); //11 con ++
  c = 10;
  print(c++); //10
  print(c); //Ya imprime el 11 con ++
}
